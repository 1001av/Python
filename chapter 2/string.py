# in python string is immutable(which cant be changed by running function on them)

# slicing in strings
a = "akhil"    
print(a[0:4])  # last index is not included

# negative slicing
a = "akhil"    
print(a[-4:-1]) # in negative index it starts from -1, but from back
# you can always convert it in normal index both will gice same output

# slicing with skip value

a = "amazing"
print(a[1:6:2]) # skip according to last values
# 1st & 2nd attribute is used to print starting & ending words, the last attribute is used to adjust jump between words 

 